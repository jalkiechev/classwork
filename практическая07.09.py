# Ввод значений R и Y в одной строке, разделенных пробелом
R, Y = map(float, input("Введите значения R и Y через пробел: ").split())

# Вычисление выражения R*Y²+(Y/5)
result = R * Y**2 + (Y / 5)

# Вывод результата в той же строке
print(f"Результат выражения {R}*{Y}²+({Y}/5) равен: {result}")
#Задача4
import math

# Ввод положительного вещественного числа A
A = float(input("Введите положительное вещественное число A: "))

# Определение целой и дробной частей числа A
whole_part = int(A)
fractional_part = A - whole_part

# Вычисление арифметического квадратного корня числа A
sqrt_A = math.sqrt(A)

# Вычисление остатка от деления числа A на 5
remainder = A % 5

# Вывод результатов
print(f"Целая часть числа A: {whole_part}")
print(f"Дробная часть числа A: {fractional_part}")
print(f"Арифметический квадратный корень числа A: {sqrt_A}")
print(f"Остаток от деления числа A на 5: {remainder}")
